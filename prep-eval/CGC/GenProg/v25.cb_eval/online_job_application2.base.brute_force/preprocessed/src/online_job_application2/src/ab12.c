int cgc_validate_email(char *input)
{
  int has_at = 0;
  char c;

  if (cgc_strlen(input) < 3 || cgc_strlen(input) > 64)
    return 0;

  for (cgc_size_t i = 0; i < cgc_strlen(input); i++) {
    c = input[i];

    if (c == '@') {
      if (has_at) {
        return 0;
      } else {
        has_at = 1;
        continue;
      }
    }

    if (!(cgc_isalnum(c) || c == '.' || c == '-' || c == '_'))
      return 0;
  }

  return has_at;
}

