int cgc_handle_update(form_t *form, char *arg)
{
  question_t *cur;
  char *input_buf;

  if (!form->cur_page->completed) {
    cgc_fdprintf(1, "Cannot update field until all fields are inputted\n");
    return 1;
  }

  if (arg == (0))
    return 1;

  cur = form->cur_page->questions;
  for(; cur != (0); cur = cur->next)
    if (cgc_strncmp(cur->title, arg, cgc_strlen(cur->title)) == 0)
      break;

  if (cur == (0)) {
    cgc_fdprintf(1, "Could not find specified field\n");
    cgc_fdprintf(1, "Update Unsuccessful\n");
    return 1;
  }

  input_buf = cgc_malloc(4096);
  if (input_buf == (0))
    return -1;

  cgc_prompt_q(cur);
  if (cgc_read_line(input_buf) < 0)
    return -1;

  if ((cgc_strlen(input_buf) == 0) && cur->optional) {
    if (input_buf != (0)) {
      cgc_free(input_buf);
    }

    if (cur->answer != (0)) {
      cgc_free(cur->answer);
    }

    cur->answer = (0);
    return 1;
  }

  if (!cur->validator(input_buf)) {
    if (input_buf != (0)) {
      cgc_free(input_buf);
    }

    cgc_fdprintf(1, "Bad input.\n");
    cgc_fdprintf(1, "Update Unsuccessful\n");
    return 1;
  }

  if (cur->answer == (0)) {
    cur->answer = input_buf;



  } else if (cgc_strncmp(cur->answer, input_buf, cgc_strlen(cur->answer)) == 0) {

    cgc_strcpy(cur->answer, input_buf);
    cgc_free(input_buf);
  } else {
    cgc_free(cur->answer);
    cur->answer = input_buf;
  }

  return 1;
}

void cgc_print_prompt(form_t *form, int with_title, int done)
{

  if (form->cur_page->title == (0)) {
    cgc_fdprintf(1, "%s", form->ending);
    return;
  }

  if (with_title)
    cgc_print_title(form);

  if (form->cur_page->completed) {
    cgc_print_page(form);
    cgc_prompt_next();
    return;
  }

  cgc_prompt_q(form->cur_question);
}

int cgc_handle_line(form_t *form, char *buf)
{
  int ret;

  if (cgc_is_cmd(form, buf))
    return cgc_handle_cmd(form, buf);

  if (form->cur_page->completed || form->cur_question == (0)) {
    return 1;
  }

  if (cgc_strlen(buf) == 0) {
    if (!form->cur_question->optional)
      return 0;
  } else {
    ret = cgc_handle_answer(form, buf);
    if (ret < 0)
      return -1;
    if (ret != 0)
      return 0;
  }

  ret = cgc_next_question(form);
  if (ret < 0)
    return -1;
  else
    return 0;

}

