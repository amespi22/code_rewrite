static int cgc_handle_cmd(form_t *form, char *buf)
{
  char *arg = cgc_malloc(cgc_strlen(buf) + 1);
  int ret;
  if (arg == (0))
    return -1;
  cgc_strcpy(arg, buf);
  arg[cgc_strlen(buf)] = '\0';
  char *cmd = cgc_strsep(&arg, " ");
  int i = cgc_handler_index(form, cmd);
  if (i < 0) {
    ret = i;
    goto out;
  }

  ret = form->handlers[i](form, arg);
out:
  if (cmd != (0)) {
    cgc_free(cmd);
  }
  return ret;
}

